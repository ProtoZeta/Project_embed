double accumError = 0;
double pid(double setpoint, double current){
	double Kp = -2.5;
	double Ki = -1;
	double Kd = -1;

	double error = setpoint - current;

	double P = Kp * error;

	accumError += error;
	double I = Ki * accumError;

	static double lastError = 0;
	double errorDiff = error - lastError;
	lastError = error;
	double D = Kd * errorDiff;

	return P+ I;// + D;
}

